#Problema 1079 - BeeCrowd - Media Ponderada

N = int(input())

for i in range(N):
    entrada = input().split(" ")
    numero1 = float(entrada[0])
    numero2 = float(entrada[1])
    numero3 = float(entrada[2])

    media = (numero1 * 2 + numero2 * 3 + numero3 * 5)/10 
    print("{:.1f}".format(media))
        
        
    
        

    






      
      



